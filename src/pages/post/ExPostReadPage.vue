import { useChatRoomStore } from "@/store/useChatRoomStore";
import { useMemberStore } from "@/store/useMemberStore";

export default {
    name: 'ExPostReadPase',
    data() {
        // const route = useRoute();
        return { 
            message: "test",
            postIdx: this.$route.params.id,
            post : {
                postIdx: 0,
                postType: "",
                status: "",
                memberIdx: 0,
                memberNickname: "",
                timeStamp: "",
                modifyTime: "",
                title: "",
                contents: "",
                giveCategory: 0,
                giveBtmCategory: "",
                takeCategory: 0,
                takeBtmCategory: "",
            }
        }
    },
    computed:{
        ...mapStores(useExchangePostStore),
    },
    async mounted() {
        await this.getPostInfo();
    },
    methods: {
        async getPostInfo() {
            // console.log(postIdx);
            
            await this.exchangePostStore.getExchangeRead(this.$route.params.id);
            this.post = this.exchangePostStore.exchangePost;
        },

        async createChatRoom(postIdx) {
            
            const memberStore = useMemberStore();

            // 본인 글이면 오류 메시지 띄우기
            if (memberStore.member.userIdx === this.post.memberIdx) {
                alert("본인 글에서는 채팅을 열 수 없습니다! 리스트에서 들어가 주세요. 감사합니다.");
                //리스트로 라우팅? 고려 중
                return;
            }

            // 본인 글이 아니면 채팅방 생성
            const chatRoomStore = useChatRoomStore();
            await chatRoomStore.CreateChatRoom(postIdx);
            
            if(chatRoomStore.roomIdx == 0) { //방 생성 안 되고 에러가 저장됐다는 뜻
                if(chatRoomStore.data.message) {
